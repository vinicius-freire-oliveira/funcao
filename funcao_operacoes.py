# Define a função soma_dois_numeros que recebe dois parâmetros e imprime a soma deles
def soma_dois_numeros(primeiro_numero, segundo_numero):
    print(primeiro_numero + segundo_numero)

# Define a função subtrai_dois_numeros que recebe dois parâmetros e imprime a diferença entre eles
def subtrai_dois_numeros(primeiro_numero, segundo_numero):
    print(primeiro_numero - segundo_numero)

# Define a função multiplica_dois_numeros que recebe dois parâmetros e imprime o produto deles
def multiplica_dois_numeros(primeiro_numero, segundo_numero):
    print(primeiro_numero * segundo_numero)

# Define a função divide_dois_numeros que recebe dois parâmetros e imprime o quociente deles
def divide_dois_numeros(primeiro_numero, segundo_numero):
    print(primeiro_numero / segundo_numero)

